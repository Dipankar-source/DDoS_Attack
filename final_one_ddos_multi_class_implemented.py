# =========================
# 2. READ ALL FILES
# =========================
dfs = []

for file in files:
    try:
        df = pd.read_csv(file)

        # Clean column names (remove spaces)
        df.columns = df.columns.str.strip()

        print(f"Loaded: {file} → Shape: {df.shape}")

        dfs.append(df)

    except Exception as e:
        print(f"Error reading {file}: {e}")

