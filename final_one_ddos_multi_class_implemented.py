def group_attack(x):
    x = str(x)

    if x == "Benign":
        return "Benign"

    elif x in ["UDP", "UDPLag", "DNS", "NTP", "SNMP", "TFTP"]:
        return "UDP_Family"

    elif x in ["MSSQL", "NetBIOS", "LDAP"]:
        return "TCP_Service"

    elif x == "Syn":
        return "SYN_Flood"

    elif x == "Portmap":
        return "Reflection"

    else:
        return "Other"

